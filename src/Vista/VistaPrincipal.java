    public boolean validarRegis(){
        boolean ban=true;
        if(txtNombre.getText().trim().isEmpty()||
           txTelHues.getText().trim().isEmpty()||
           txDireccHue.getText().trim().isEmpty()){
            ban=false;
            limpiarRes();
            JOptionPane.showMessageDialog(null,"ERROR CAMOPS BASIO");
        }
        return ban;
    }
    
    public void actualizarListasHOteles(){
    jHotel1.setListData(ch.listaHoteles().toArray());
    jHotel2.setListData(ch.listaHoteles().toArray());
    jHotel3.setListData(ch.listaHoteles().toArray());
    }
    
    public void limpiarRes(){
    txDireccHue.setText("");
    txTelHues.setText("");
    txtNombre.setText("");
    }
    
    public void limpiarHotel(){
    txDireccion.setText("");
    TxnombreCiudad.setText("");
    txTelHotel.setText("");
    txNombreHotel.setText("");
    }
    
