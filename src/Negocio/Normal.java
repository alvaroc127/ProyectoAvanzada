package Negocio;

/**
 *
 * @author felipe
 */
public class Normal implements Descuento {
    private int porcentaje;

    public Normal() {
    }

    public Normal(int porcentaje) {
        this.porcentaje = porcentaje;
    }

    public int getPorcentaje() {
        return porcentaje;
    }

    public void setPorcentaje(int porcentaje) {
        this.porcentaje = porcentaje;
    }

    @Override
    public int returnaDesc() {
        return porcentaje;
    } 
}
